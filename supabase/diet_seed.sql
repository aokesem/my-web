-- ============================================================
-- 1. 创建 diet_foods 食物表
-- ============================================================
CREATE TABLE IF NOT EXISTS public.diet_foods (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL,
    category text NOT NULL,
    rating numeric(3, 1) DEFAULT 0,
    image_url text,
    notes text,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- ============================================================
-- 2. 创建 diet_restaurants 餐厅表
-- ============================================================
CREATE TABLE IF NOT EXISTS public.diet_restaurants (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL,
    category text NOT NULL,
    address text,
    rating numeric(3, 1) DEFAULT 0,
    notes text,
    images text[],
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- ============================================================
-- 3. 创建 diet_recipes 食谱表
-- ============================================================
CREATE TABLE IF NOT EXISTS public.diet_recipes (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    name text NOT NULL,
    category text NOT NULL,
    type text NOT NULL CHECK (type IN ('can_cook', 'favorite')),
    rating numeric(3, 1) DEFAULT 0,
    image_url text,
    notes text,
    restaurant_id bigint REFERENCES public.diet_restaurants(id) ON DELETE SET NULL,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- ============================================================
-- 4. 创建 diet_recipe_foods (食谱-食材 多对多关联表)
-- ============================================================
CREATE TABLE IF NOT EXISTS public.diet_recipe_foods (
    recipe_id bigint REFERENCES public.diet_recipes(id) ON DELETE CASCADE,
    food_id bigint REFERENCES public.diet_foods(id) ON DELETE CASCADE,
    PRIMARY KEY (recipe_id, food_id)
);

-- ============================================================
-- 5. 创建 diet_restaurant_dishes 餐厅推荐菜单表
-- ============================================================
CREATE TABLE IF NOT EXISTS public.diet_restaurant_dishes (
    id bigint PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    restaurant_id bigint REFERENCES public.diet_restaurants(id) ON DELETE CASCADE NOT NULL,
    name text NOT NULL,
    image_url text,
    recipe_id bigint REFERENCES public.diet_recipes(id) ON DELETE SET NULL,
    sort_order integer DEFAULT 0,
    created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL
);

